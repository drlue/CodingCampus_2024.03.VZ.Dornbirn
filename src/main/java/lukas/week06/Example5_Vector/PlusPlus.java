package lukas.week06.Example5_Vector;

public class PlusPlus {
    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        System.out.println("a: " + a);
        System.out.println("b: " + b);

        System.out.println("a: " + ++a);
        System.out.println("b: " + b++);


        System.out.println("a: " + a);
        System.out.println("b: " + b);

    }
}
